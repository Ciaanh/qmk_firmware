bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case VS_FRM:
        if (record->event.pressed) {
            SEND_STRING(SS_LCTL("kdke"));
        } else {
        }
        break;

    case _COMM:
        if (record->event.pressed) {
            SEND_STRING(SS_LCTL("kc"));
        } else {
        }
        break;

    case _UNCOMM:
        if (record->event.pressed) {
            SEND_STRING(SS_LCTL("ku"));
        } else {
        }
        break;
  }
  return true;
}



